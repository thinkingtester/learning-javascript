// For Loop

// First part- creates a counter variable
// Second part- sets the condition in which the loop will execute
// Third part- increments the counter variable

for(x = 1; x < 5; x++) {
    console.log("x is " + x)
}

let y = "foo"

for(x = 1; x < 5; x++) {
    y = y + "bar"
    console.log(y)
}